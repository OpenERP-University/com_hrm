			//Do any procesing on fields here if needed

		}

		return $item;
	}

	/**
	 * Prepare and sanitise the table prior to saving.
	 *
	 * @since	1.6
	 */
	protected function prepareTable($table)
	{
		jimport('joomla.filter.output');

		if (empty($table->id)) {

			// Set ordering to the last item if not set
			if (@$table->ordering === '') {
				$db = JFactory::getDbo();
				$db->setQuery('SELECT MAX(ordering) FROM #__hrm_wage');
				$max = $db->loadResult();
				$table->ordering = $max+1;
			}

		}
	}

	 public function getListWare($ware_max = NULL) {
        if ($ware_max) {
            $db = JFactory::getDbo();
            $query = $db->getQuery(true);
            $query
                    ->select(array($db->quoteName('title'), $db->quoteName('guid')))
                    ->from('`#__hrm_wage`')
                    ->where($db->quoteName('title') . ' <= ' . $db->quote($db->escape($ware_max)));

            $db->setQuery($query);

            $results = $db->loadAssocList();

            if ($results) {
                return $results;
            } else {
                return FALSE;
            }
        } else {
            return FALSE;
        }
    }
}